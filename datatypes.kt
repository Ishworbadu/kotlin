fun main()
{
    // var,val keyword used to make variable
    // val makes constant variable
    // var makes variable variable 
    var name="harry"
    val age=22
    println("the name is "+name)//here name and the string are concatenated
    println("the name is {$name} ${name}")//this {$name } is called use of template
    println("the age is {$age}")
    println("the age is "+age)

    // addition of two numbers
    var a=1
    var b=3
    println("the sum of $a and $b is ${a+b}")

    // to define specified type of datatype use var var_name:type=value
     

}